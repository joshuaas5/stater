export const useTranslation = () => {
  const { language } = getUserPreferences();
  const currentLang = language || 'pt-BR';

  const t = useCallback((key: string): string => {
    return translations[currentLang]?.[key] || key;
  }, [currentLang]);

  return { t, currentLang };
};
